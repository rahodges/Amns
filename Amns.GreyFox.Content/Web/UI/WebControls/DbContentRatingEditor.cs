using System;
using System.Data;
using System.Web.UI;
using System.ComponentModel;
using System.Web.UI.WebControls;
using Amns.GreyFox.Security;
using Amns.GreyFox.Web.UI.WebControls;

namespace Amns.GreyFox.Content.Web.UI.WebControls
{
	/// <summary>
	/// Default web editor for DbContentRating.
	/// </summary>
	[DefaultProperty("ConnectionString"), 
		ToolboxData("<{0}:DbContentRatingEditor runat=server></{0}:DbContentRatingEditor>")]
	public class DbContentRatingEditor : TableWindow, INamingContainer
	{
		private int dbContentRatingID;
		private DbContentRating editDbContentRating;
		private string connectionString;
		private bool loadFlag = false;
		private bool resetOnAdd;
		private bool editOnAdd;

		#region Private Control Fields for _system Folder

		private Literal ltCreateDate = new Literal();
		private Literal ltModifyDate = new Literal();

		#endregion

		#region Private Control Fields for General Folder

		private TextBox tbName = new TextBox();
		private TextBox tbDescription = new TextBox();
		private TextBox tbIconUrl = new TextBox();
		private MultiSelectBox msRequiredRole = new MultiSelectBox();

		#endregion

		private Button btOk = new Button();
		private Button btCancel = new Button();
		private Button btDelete = new Button();

		#region Public Control Properties

		[Bindable(true), Category("Data"), DefaultValue(0)]
		public int DbContentRatingID
		{
			get
			{
				return dbContentRatingID;
			}
			set
			{
				loadFlag = true;
				dbContentRatingID = value;
			}
		}

		[Bindable(true), Category("Behavior"), DefaultValue(false)]
		public bool ResetOnAdd
		{
			get
			{
				return resetOnAdd;
			}
			set
			{
				resetOnAdd = value;
			}
		}

		[Bindable(true), Category("Behavior"), DefaultValue(false)]
		public bool EditOnAdd
		{
			get
			{
				return editOnAdd;
			}
			set
			{
				editOnAdd = value;
			}
		}

		[Bindable(false),
			Category("Data"),
			DefaultValue("")]
		public string ConnectionString
		{
			get
			{
				return connectionString;
			}
			set
			{
				// Parse Connection String
				if(value.StartsWith("<jet40virtual>") & Context != null)
					connectionString = "PROVIDER=Microsoft.Jet.OLEDB.4.0;DATA SOURCE=" +
						Context.Server.MapPath(value.Substring(14, value.Length - 14));
				else if(value.StartsWith("<jet40config>") & Context != null)
					connectionString = "PROVIDER=Microsoft.Jet.OLEDB.4.0;DATA SOURCE=" +
						Context.Server.MapPath(System.Configuration.ConfigurationManager.AppSettings.Get(value.Substring(13, value.Length - 13)));
				else
					connectionString = value;
			}
		}

		#endregion

		protected override void CreateChildControls()
		{
			Controls.Clear();
			bindDropDownLists();

			#region Child Controls for _system Folder

			ltCreateDate.EnableViewState = false;
			Controls.Add(ltCreateDate);

			ltModifyDate.EnableViewState = false;
			Controls.Add(ltModifyDate);

			#endregion

			#region Child Controls for General Folder

			tbName.EnableViewState = false;
			Controls.Add(tbName);

			tbDescription.EnableViewState = false;
			Controls.Add(tbDescription);

			tbIconUrl.EnableViewState = false;
			Controls.Add(tbIconUrl);

			msRequiredRole.Mode = MultiSelectBoxMode.DropDownList;
			Controls.Add(msRequiredRole);

			#endregion

			btOk.Text = "OK";
			btOk.Width = Unit.Pixel(72);
			btOk.EnableViewState = false;
			btOk.Click += new EventHandler(ok_Click);
			Controls.Add(btOk);

			btCancel.Text = "Cancel";
			btCancel.Width = Unit.Pixel(72);
			btCancel.EnableViewState = false;
			btCancel.Click += new EventHandler(cancel_Click);
			Controls.Add(btCancel);

			btDelete.Text = "Delete";
			btDelete.Width = Unit.Pixel(72);
			btDelete.EnableViewState = false;
			btDelete.Click += new EventHandler(delete_Click);
			Controls.Add(btDelete);

			ChildControlsCreated = true;
		}

		private void bindDropDownLists()
		{
			#region Bind General Child Data

			GreyFoxRoleManager requiredRoleManager = new GreyFoxRoleManager();
			GreyFoxRoleCollection requiredRoleCollection = requiredRoleManager.GetCollection(string.Empty, string.Empty);
			foreach(GreyFoxRole requiredRole in requiredRoleCollection)
			{
				ListItem i = new ListItem(requiredRole.ToString(), requiredRole.ID.ToString());
				msRequiredRole.Items.Add(i);
			}

			#endregion

		}
		#region ok_Click Save and Update Apply

		protected void ok_Click(object sender, EventArgs e)
		{
			if(dbContentRatingID == 0)
				editDbContentRating = new DbContentRating();
			else
				editDbContentRating = new DbContentRating(dbContentRatingID);

			editDbContentRating.Name = tbName.Text;
			editDbContentRating.Description = tbDescription.Text;
			editDbContentRating.IconUrl = tbIconUrl.Text;

			if(msRequiredRole.SelectedItem != null)
				editDbContentRating.RequiredRole = GreyFoxRole.NewPlaceHolder( 
					int.Parse(msRequiredRole.SelectedItem.Value));
			else
				editDbContentRating.RequiredRole = null;

			if(editOnAdd)
				dbContentRatingID = editDbContentRating.Save();
			else
				editDbContentRating.Save();

			if(resetOnAdd)
			{
				tbName.Text = string.Empty;
				tbDescription.Text = string.Empty;
				tbIconUrl.Text = string.Empty;
				msRequiredRole.SelectedIndex = 0;
			}

			OnUpdated(EventArgs.Empty);
		}

		#endregion

		protected void cancel_Click(object sender, EventArgs e)
		{
			this.OnCancelled(EventArgs.Empty);
		}

		protected void delete_Click(object sender, EventArgs e)
		{
			this.OnDeleteClicked(EventArgs.Empty);
		}

		public event EventHandler Cancelled;
		protected virtual void OnCancelled(EventArgs e)
		{
			if(Cancelled != null)
				Cancelled(this, e);
		}

		public event EventHandler Updated;
		protected virtual void OnUpdated(EventArgs e)
		{
			if(Updated != null)
				Updated(this, e);
		}

		public event EventHandler DeleteClicked;
		protected virtual void OnDeleteClicked(EventArgs e)
		{
			if(DeleteClicked != null)
			DeleteClicked(this, e);
		}

		protected override void OnInit(EventArgs e)
		{
			columnCount = 2;
			features = TableWindowFeatures.DisableContentSeparation;
			components = TableWindowComponents.Tabs;
			tabStrip = new TabStrip();
			tabStrip.Tabs = new TabList();

			Tab GeneralTab = new Tab("General");
			GeneralTab.RenderDiv += new TabRenderHandler(renderGeneralFolder);
			GeneralTab.Visible = true;
			tabStrip.Tabs.Add(GeneralTab);

		}

		protected override void OnPreRender(EventArgs e)
		{
			if(dbContentRatingID != 0 & loadFlag)
			{
				editDbContentRating = new DbContentRating(dbContentRatingID);

				//
				// Set Field Entries
				//
				ltCreateDate.Text = editDbContentRating.CreateDate.ToString();
				ltModifyDate.Text = editDbContentRating.ModifyDate.ToString();
				tbName.Text = editDbContentRating.Name;
				tbDescription.Text = editDbContentRating.Description;
				tbIconUrl.Text = editDbContentRating.IconUrl;

				//
				// Set Children Selections
				//
				if(editDbContentRating.RequiredRole != null)
					foreach(ListItem item in msRequiredRole.Items)
						item.Selected = editDbContentRating.RequiredRole.ID.ToString() == item.Value;

				text = "Edit Rating - " + editDbContentRating.ToString();
			}
			else
				text = "Add Rating";
		}

		protected override void RenderContent(HtmlTextWriter output)
		{
			RenderTabPanels(output);
			//
			// Render OK/Cancel Buttons
			//
			output.WriteFullBeginTag("tr");
			output.WriteBeginTag("td");
			output.WriteAttribute("colspan", "2");
			output.WriteAttribute("align", "right");
			output.WriteAttribute("class", "row1");
			output.Write(HtmlTextWriter.TagRightChar);
			btOk.RenderControl(output);
			output.Write("&nbsp;");
			btCancel.RenderControl(output);
			if(DeleteClicked != null)
			{
				output.Write(" ");
				btDelete.RenderControl(output);
			}
			output.WriteEndTag("td");
			output.WriteEndTag("tr");
		}

